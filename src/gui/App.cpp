#include "App.h"
#include <QMainWindow>
#include "SettingsStore.h"

QMainWindow* App::_mainWnd = NULL;
SettingsStore* App::_settings = NULL;

//=======================================================================
//=======================================================================
void App::mainWnd(QMainWindow *w)
{
    _mainWnd = w;
}

//=======================================================================
//=======================================================================
QMainWindow* App::mainWnd()
{
    return _mainWnd;
}

//=======================================================================
//=======================================================================
void App::settings(SettingsStore *s)
{
   _settings = s;
}

//=======================================================================
//=======================================================================
SettingsStore* App::settings()
{
    return _settings;
}
